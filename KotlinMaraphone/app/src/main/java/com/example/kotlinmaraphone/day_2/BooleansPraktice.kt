package com.example.kotlinmaraphone.day_2

fun main() {
    val myTrue = true
    val myFalse = false
    println(myTrue || myFalse) // знак || - обозначает или, в случае если одно из значений будет true, то выведет true
    println(myTrue && myFalse) // знак && - обозначает и, в случае если оба значения будут true, то выведет true
    println(!myTrue) // знак ! перед перемнной обозначает отрицание в случае с !true = false
    println(myTrue) // This will be true
}